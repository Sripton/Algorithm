//  Задача: найти все ключи в объекте рекурсивно javascript 
// Вы можете реализовать это, создав функцию, которая пройдет по каждому свойству объекта и проверит, является ли это свойство объектом. Если это так, вы будете рекурсивно вызывать эту функцию для этого свойства. 
// Здесь функция, которая делает это:
function getAllKeys(obj) {
    let keys = [];

    for(let key in obj) {
        keys.push(key);

        if(typeof obj[key] === "object" && obj[key] !== null) {
            keys = keys.concat(getAllKeys(obj[key]));
        }
    }

    return keys;
}
//  пример использования
let obj = {
    a: 1,
    b: 2,
    c: {
        d: 3,
        e: {
            f: 4,
            g: 5
        }
    }
};

console.log(getAllKeys(obj)); // ["a", "b", "c", "d", "e", "f", "g"];

// Обратите внимание, что функция getAllKeys использует цикл for...in для перебора всех свойств в объекте. 
// Если свойство является объектом (и не null), она рекурсивно вызывает getAllKeys для этого свойства.

