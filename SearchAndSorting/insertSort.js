// Сортировка вставками (insertion sort) — это алгоритм сортировки, который строит отсортированный 
// массив (или список) одним элементом за раз. Это происходит путем взятия одного элемента, 
// нахождения подходящего места для него в уже отсортированной части списка и вставки туда.

// Вот пример сортировки вставками на JavaScript:

function insertionSort(arr) {
    let len = arr.length;
    for (let i = 1; i < len; i++) {
        let key = arr[i];
        let j = i - 1;
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j = j - 1;
        }
        arr[j + 1] = key;
    }
    return arr;
}

// использование функции
let arr = [64, 34, 25, 12, 22, 11, 90];
insertionSort(arr);
console.log(arr);

// Этот код работает путем преобразования исходного массива arr в отсортированный массив. 
// На каждом шаге он берет следующий элемент (key) и вставляет его на подходящее место в уже 
// отсортированной части массива. Если текущий элемент arr[j] больше key, он сдвигается на одну позицию вперед. 
// Это продолжается до тех пор, пока не будет найдено место для key или пока не будет достигнут начало массива. 
// Затем key вставляется на найденное место.